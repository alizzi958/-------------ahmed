package com.wassup741.excel.comparator.common.border;

public class BorderInfo {
	public short top;
	public String topColor;
	public short right;
	public String rightColor;
	public short bottom;
	public short left;

	public BorderInfo(short top, short right, short bottom, short left) {
		super();
		this.top = top;
		this.right = right;
		this.bottom = bottom;
		this.left = left;
	}

}
